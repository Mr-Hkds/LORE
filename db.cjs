// Load the image manifest generated by generate_stories_ai.cjs after each run.
// This is the ground-truth list of filenames actually committed to the git repo.
let _imageManifestSet = null;
function getImageManifest() {
  if (_imageManifestSet !== null) return _imageManifestSet;
  try {
    const manifestPath = path.join(__dirname, 'public', 'content', 'image_manifest.json');
    const data = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
    _imageManifestSet = new Set(Array.isArray(data.committed) ? data.committed : []);
  } catch {
    // manifest not yet generated (first run), treat as empty
    _imageManifestSet = new Set();
  }
  return _imageManifestSet;
}

// Returns true if a /content/images/<filename> path is considered valid on this environment.
function isLocalImageValid(filename) {
  if (EXISTING_LOCAL_IMAGES.has(filename)) return true;
  if (getImageManifest().has(filename)) return true;
  // On non-Vercel (local dev), also check the filesystem directly
  if (!isVercel) {
    const absolutePath = path.join(__dirname, 'public', 'content', 'images', filename);
    return fs.existsSync(absolutePath);
  }
  return false; // Vercel: file not in manifest → treat as missing
}

