async function exportStoriesToJSON() {
  if (isVercel) {
    console.log('[DB] Running on Vercel. Skipping static file export (GitHub Sync should be used for persistence).');
    return;
  }
  const STORIES_FILE = path.join(__dirname, 'public', 'content', 'stories.json');
  const CONCEPTS_FILE = path.join(__dirname, 'public', 'content', 'concept_index.json');
  const STORIES_DIR = path.join(__dirname, 'public', 'content', 'stories');
  
  if (!fs.existsSync(STORIES_DIR)) {
    fs.mkdirSync(STORIES_DIR, { recursive: true });
  }

  // 1. Export metadata list (includeFullContent = false)
  const metadataStories = await getStories(false, false);
  
  // 2. Export full stories (includeFullContent = true) to individual files
  const fullStoriesList = await getStories(false, true);

  try {
    fs.writeFileSync(STORIES_FILE, JSON.stringify({ stories: metadataStories }, null, 2));
    
    for (const story of fullStoriesList) {
      const storyFile = path.join(STORIES_DIR, `${story.story_id}.json`);
      fs.writeFileSync(storyFile, JSON.stringify(story, null, 2));
    }
    
    const conceptIndex = {};
    metadataStories.forEach(story => {
      if (story.concepts) {
        story.concepts.forEach(concept => {
          const c = concept.trim().toLowerCase();
          if (!conceptIndex[c]) {
            conceptIndex[c] = [];
          }
          if (!conceptIndex[c].includes(story.story_id)) {
            conceptIndex[c].push(story.story_id);
          }
        });
      }
    });
    fs.writeFileSync(CONCEPTS_FILE, JSON.stringify(conceptIndex, null, 2));
    console.log(`[DB] Successfully exported ${metadataStories.length} live story metadata entries and individual story JSONs.`);
  } catch (err) {
    console.error('[DB] Failed to export stories to static JSON files:', err.message);
  }
}

function fetchGitHubStoriesJson() {
  return new Promise((resolve, reject) => {
    const url = 'https://raw.githubusercontent.com/Mr-Hkds/LORE/main/public/content/stories.json';
    https.get(url, { headers: { 'User-Agent': 'SevenDescents/2.0' }, timeout: 5000 }, (res) => {
      if (res.statusCode !== 200) {
        return reject(new Error(`HTTP ${res.statusCode}`));
      }
      let body = '';
      res.on('data', chunk => body += chunk);
      res.on('end', () => {
        try {
          resolve(JSON.parse(body));
        } catch (e) {
          reject(e);
        }
      });
    }).on('error', reject);
  });
}

async function seed() {
  await ensureInit();
  
  const STORIES_FILE = path.join(__dirname, 'public', 'content', 'stories.json');
  const RECOMMENDATIONS_FILE = path.join(__dirname, 'public', 'content', 'recommendations.json');
  const FEEDBACK_FILE = path.join(__dirname, 'public', 'content', 'feedback.json');

  let data = null;
  // Try fetching raw stories.json from GitHub first to incorporate skips immediately
  try {
    console.log('[DB SEED] Attempting to fetch seed data from GitHub raw URL...');
    data = await fetchGitHubStoriesJson();
    console.log('[DB SEED] Successfully fetched seed data from GitHub.');
  } catch (ghErr) {
    console.warn('[DB SEED] GitHub raw fetch failed, falling back to local file:', ghErr.message);
    try {
      data = require('./public/content/stories.json');
    } catch (err) {
      if (fs.existsSync(STORIES_FILE)) {
        data = JSON.parse(fs.readFileSync(STORIES_FILE, 'utf8'));
      }
    }
  }
  const resStoriesCount = await client.execute('SELECT COUNT(*) as count FROM stories');
  const dbStoriesCount = resStoriesCount.rows[0]?.count || 0;

  if (data && Array.isArray(data.stories)) {
    try {
      const validStories = data.stories.filter(s => s && s.story_id && s.story_id !== 'null' && s.story_id !== 'undefined');
      const isInitialSeed = dbStoriesCount === 0;
      if (isInitialSeed) {
        console.log(`Database is empty. Initializing with ${validStories.length} stories from stories.json...`);
      } else {
        console.log(`Database already has ${dbStoriesCount} stories. Running incremental check (INSERT OR IGNORE)...`);
      }

      const batchStmts = validStories.map(s => ({
        sql: `${isInitialSeed ? 'INSERT OR REPLACE' : 'INSERT OR IGNORE'} INTO stories (
          story_id, title, category, hook, concepts, severity, hero_image, added_date, draft, reactions, evidence_links, connections, layers, vocabulary, year
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        args: [
          s.story_id || null,
          s.title || null,
          s.category || null,
          s.hook || null,
          JSON.stringify(s.concepts || []),
          s.severity || null,
          s.hero_image || null,
          s.added_date || null,
          s.draft ? 1 : 0,
          JSON.stringify(s.reactions || { gripping: 0, scared: 0, mindblown: 0, like: 0 }),
          JSON.stringify(s.evidence_links || []),
          JSON.stringify(s.connections || []),
          JSON.stringify(s.layers || []),
          JSON.stringify(s.vocabulary || {}),
          s.year || 'N/A'
        ]
      }));

      // Delete any extra stories from database ONLY if this is the initial seed alignment
      if (isInitialSeed) {
        const idsInJson = validStories.map(s => s.story_id);
        if (idsInJson.length > 0) {
          const placeholders = idsInJson.map(() => '?').join(',');
          batchStmts.push({
            sql: `DELETE FROM stories WHERE story_id NOT IN (${placeholders})`,
            args: idsInJson
          });
        } else {
          batchStmts.push({
            sql: `DELETE FROM stories`,
            args: []
          });
        }
      }

      await client.batch(batchStmts, "write");
      console.log(`Database stories alignment complete.`);
    } catch (e) {
      console.error('Failed to align stories in database:', e);
    }
  }

  const resRecCount = await client.execute('SELECT COUNT(*) as count FROM recommendations');
  const recCount = resRecCount.rows[0]?.count || 0;
  if (recCount === 0) {
    try {
      let recs = null;
      try {
        recs = require('./public/content/recommendations.json');
      } catch (err) {
        if (fs.existsSync(RECOMMENDATIONS_FILE)) {
          recs = JSON.parse(fs.readFileSync(RECOMMENDATIONS_FILE, 'utf8'));
        }
      }

      if (Array.isArray(recs)) {
        console.log(`Seeding ${recs.length} recommendations to database...`);
        const batchStmts = recs.map(r => ({
          sql: 'INSERT INTO recommendations (id, topic, date, status) VALUES (?, ?, ?, ?)',
          args: [r.id, r.topic, r.date, r.status || 'pending']
        }));
        await client.batch(batchStmts, "write");
      }
    } catch (e) {
      console.error('Failed to seed recommendations:', e);
    }
  }

  const resFbCount = await client.execute('SELECT COUNT(*) as count FROM feedback');
  const fbCount = resFbCount.rows[0]?.count || 0;
  if (fbCount === 0) {
    try {
      let feedback = null;
      try {
        feedback = require('./public/content/feedback.json');
      } catch (err) {
        if (fs.existsSync(FEEDBACK_FILE)) {
          feedback = JSON.parse(fs.readFileSync(FEEDBACK_FILE, 'utf8'));
        }
      }

      if (Array.isArray(feedback)) {
        console.log(`Seeding ${feedback.length} feedback entries to database...`);
        const batchStmts = feedback.map(f => ({
          sql: 'INSERT INTO feedback (id, rating, tags, note, timestamp, page, addressed) VALUES (?, ?, ?, ?, ?, ?, ?)',
          args: [
            f.id,
            f.rating,
            JSON.stringify(f.tags || []),
            f.note || '',
            f.timestamp || new Date().toISOString(),
            f.page || '/',
            f.addressed ? 1 : 0
          ]
        }));
        await client.batch(batchStmts, "write");
      }
    } catch (e) {
      console.error('Failed to seed feedback:', e);
    }
  }
}

