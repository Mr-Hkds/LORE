async function updateStory(story_id, updates) {
  const existing = await getStory(story_id);
  if (!existing) return false;
  
  const today = new Date().toLocaleDateString('en-CA');
  if (updates.hero_image && (!existing.added_date || existing.added_date === '2026-01-01')) {
    updates.added_date = today;
  }
  
  const merged = { ...existing, ...updates };
  await insertStory(merged);
  return true;
}

